// 128 unique ASCII emoticons for comment avatars

const AVATARS: string[] = [
  // 0-15: Classic
  "^_^", "o_o", ">_<", "-_-", "*_*", "=_=", "~_~", "._.",
  ";_;", "T_T", "x_x", "O_O", "@_@", "$_$", "#_#", "+_+",
  // 16-31: Dash mouth
  "^-^", "o-o", ">-<", "*-*", "=-=", "~-~", ";-;", "+-+",
  "O-O", "@-@", "$-$", "#-#", "x-x", "T-T", "0-0", "!-!",
  // 32-47: Tilde mouth
  "^~^", "o~o", ">~<", "*~*", "=~=", ";~;", "+~+", "O~O",
  "@~@", "$~$", "#~#", "x~x", "T~T", ".~.", "0~0", "!~!",
  // 48-63: Dot mouth
  "^.^", "o.o", ">.<", "*.*", "=.=", ";.;", "+.+", "O.O",
  "@.@", "$.$ ", "#.#", "x.x", "T.T", "-.+", "0.0", "!.!",
  // 64-79: w mouth (cute)
  "^w^", "owo", ">w<", "*w*", "=w=", ";w;", "+w+", "OwO",
  "@w@", "$w$", "#w#", "xwx", "TwT", ".w.", "0w0", "!w!",
  // 80-95: v mouth
  "^v^", "ovo", ">v<", "*v*", "=v=", ";v;", "+v+", "OvO",
  "@v@", "$v$", "#v#", "xvx", "TvT", ".v.", "0v0", "!v!",
  // 96-111: Asymmetric / winks
  "^_o", "o_^", ";_^", "^_;", "*_o", "o_*", "~_o", "o_~",
  ">_o", "o_<", "O_o", "o_O", "0_o", "o_0", "@_o", "o_@",
  // 112-127: Special
  ">:)", ":3 ", ":D ", ":P ", ":/ ", ":| ", ":O ", ";) ",
  ">()", "<>_<>", "=3=", "^3^", "o3o", ";3;", "*3*", "uwu",
];

export function getAvatar(id: number): string {
  return AVATARS[id % AVATARS.length].trim();
}
